package javafxapplication2;
public class MoistureSensor
{
    
    
    public double getTomatoMoisture(Tomato t)
    {
        return t.getMoistureLevel();
    }
    public double getPotatoMoisture(Potato p)
    {
        return p.getMoistureLevel();
    }
    public double getBasilMoisture(Basil b)
    {
        return b.getMoistureLevel();
    }
    public double getLettuceMoisture(Lettuce l)
    {
        return l.getMoistureLevel();
    }
    public double getStrawberryMoisture(Strawberry s)
    {
       return s.getMoistureLevel();
    }
    public double getBlueberryMoisture(Blueberry b)
    {
       return b.getMoistureLevel();
    }
}
